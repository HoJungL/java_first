package variable;

public class Var7 {

    public static void main(String[] args) {
        int a = 100 ; //정수
        double b = 10.5; //실수
        boolean c = true; // 불리언 (T,F 입력가능)
        char d = 'A';  // 문자 하나
        String e = "Hello Java"; // 문자 장문 가능, 큰따옴표 써야함!!!

        System.out.println(a); //컨트롤 d 누르면 바로 복붙됨
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
/*
리터럴
- 코드에서 개발자가 직접 적은 10, 10.5,'A' 와 같은 고정된 값을
프로그래밍 용어로 리터럴이라고 함.
변수의 값은 변할수 있지만, 리터럴은 개발자가 직접 입력한 고정된 값이므로, 리터럴 자체는 변하지 않음.
literal: 단어의 어원이 문자 or 글자를 뜻함.

 */